export class Token {
  token: string;

  constructor(token: string) {
    this.token = token;
  }
}


export class Point {
  xVal: number;
  yVal: number;
  rVal: number;

  constructor(xVal: number, yVal: number, rVal: number) {
    this.xVal = xVal;
    this.yVal = yVal;
    this.rVal = rVal;
  }
}

